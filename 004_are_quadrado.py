#!/usr/bin/env python
#Faca um Programa que calcule a area de um quadrado, em seguida mostre o dobro desta area para o usuario.

area = float(input("Insert area do quadrado: "))
print("Area do quadrado inserida %r") % (area)
mult = (area*area)
area_total = (mult)
print("area do quadrado: %r") % (area_total)
dobro = (area_total + area_total)
print("Dobro do area : %r") % (dobro)
